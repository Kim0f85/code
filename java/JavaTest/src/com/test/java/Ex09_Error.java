package com.test.java;

public class Ex09_Error {
	public static void main(String[] args) {
		//Ex09_Error.java
		
		int a = 10; //사용자가 입력
		
		//컴파일 전에 나는 오류 : 컴파일 오류 (compile error)
		//프로그램이 실행하면서 나는 오류 : 런타임 오류 (runtime error)
		
		System.out.println(100/a);
		/*
			에러, error
			- 오류, 버그(Bug), 예외(Exception) 등......
			
			1. 컴파일 에러 
				- 컴파일 작업중에 발생하는 에러 
				- 컴파이러 발견 > 번역하다가..> 문법이 틀려서 
				- 발생 > 컴파일 작업 중단 > 프로그램 생서 불가 > 반드시 해결(100%)
				- 난이도 가장 낮음 > 컴파이러가 알려줌 > 에러 메시지 
				- 오타 
				- 빨간 밑줄 
				
			2. 런타임 에러 
				- 런타임(runtime) > 실행중
				- 컴파일 작업 중에는 없었는데.. 실행 중에 발생하는 
				- 문법에는 오류가 없었는데.. 다른 원인으로 발생하는 
				- 예외(Exception)
				- 난이도 중간 > 발견 O,X (복불복)
				
				
			3. 논리 에러 
				- 컴파일 성공 + 실행 성공 > 결과가 이상
				
		
		
		
		*/
		
		
		
		
	}
}
